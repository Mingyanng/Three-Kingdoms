package CommonalityObject;

public class RGBColor {
	public int R;
	public int G;
	public int B;


	public RGBColor(int i, int j, int k) {
		R = i;
		G= j;
		B = k;
	}

	public int getR() {
		return R;
	}

	public void setR(int r) {
		R = r;
	}

	public int getG() {
		return G;
	}

	public void setG(int g) {
		G = g;
	}

	public int getB() {
		return B;
	}

	public void setB(int b) {
		B = b;
	}

}
